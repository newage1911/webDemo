            <a id="q11" onclick="myFunction11()">11. เครื่องมือที่ให้บริการอาชีวเวชกรรม หรืออาชีวเวชศาสตร์
                ต้องมีคุณลักษณะอย่างไร</a>
            <div id="ans11">1.เครื่องตรวจสมรรถภาพการได้ยิน เป็นเครื่องที่สามารถส่งสัญญาณเสียงบริสุทธิ์
                ได้รับการรับรองตามมาตรฐาน American National Standards Institute (ANSI) S3.6-1996 หรือปีที่ใหม่กว่า
                โดยหน่วยบริการต้องมีเอกสารแสดงการสอบเทียบเครื่องมือตามระยะเวลาที่กำหนดไม่เกิน 2 ปี
                ห้อง หรือพื้นที่ที่ทำการตรวจสมรรถภาพการได้ยิน ต้องมีระดับเสียงขั้นต่ำตาม American National Standards
                Institute (ANSI) S3.1-1999 หรือ OSHA 1983 หรือมาตรฐานอื่นๆ ที่ไม่ต่ำกว่ามาตรฐานดังกล่าวข้างต้น
                ตู้ตรวจการได้ยิน (audio booth) ต้องมีระดับเสียงขั้นต่ำตาม American National Standards Institute (ANSI)
                S3.1-1999 หรือปีที่ใหม่กว่า <br>
                2 เครื่องตรวจสมรรถภาพปอดได้รับรองตามมาตรฐาน American Thoracic Society (ATS) ปี ค.ศ. 1994
                หรือปีที่ใหม่กว่า หรือ European Respiratory Society Task Force (ATS/ERS) ปี ค.ศ. 2005
                หรือเป็นไปตามมาตรฐานอื่นที่ไม่ต่ำกว่ามาตรฐานดังกล่าวข้างต้น ทั้งนี้
                เครื่องตรวจต้องสามารถแสดงผลสไปโรแกรมได้ทั้ง Volume-time curve และ Flow-volume curve
                รวมทั้งต้องสามารถรายงานผลครบทุกพารามิเตอร์ ดังนี้ FVC, FEV1, และ FEV1/FVC ทั้งค่า Measure และ %Predicted
                พร้อมทั้งมีอุปกรณ์สำหรับการปรับความเที่ยงตรงประจำวัน (daily calibration) เช่น กระบอกสูบ <br>
                3 เครื่องตรวจสมรรถภาพการมองเห็น เป็นการวัดความสามารถของสายตาในเรื่องต่างๆ ทั้งในระยะใกล้
                (ระยะที่วัตถุอยู่ห่างจากสายตา 14 นิ้ว) และระยะไกล (ระยะที่วัตถุอยู่ห่างจากสายตา 20 ฟุต)
                เครื่องตรวจสมรรถภาพสายตาต้องมีสไลด์ชุดสำหรับงานอาชีวอนามัย หรือชุดมาตรฐาน ที่สามารถทดสอบในเรื่องต่าง ๆ
                เหล่านี้ <br>
                1.ทดสอบสายตาเข (phoria) <br>
                2.ทดสอบสายตาตามแนวดิ่ง (vertical) <br>
                3.ทดสอบสายตาตามแนวนอน (lateral) <br>
                4.ทดสอบความชัดของภาพ (acuity) br
                5.ทดสอบความลึกของภาพ (dept. perception) <br>
                6.ทดสอบการแยกสี (color) <br>
                โดยผลการตรวจวัดต้องสามารถเทียบกับลักษณะงานได้
                4.เครื่องมือด้านอาชีวเวชศาสตร์อื่นๆ เครื่องมือนั้นต้องเป็นไปตามมาตรฐาน
            </div>
